package day1;

public class FloatAndDouble {

	public static void main(String[] args) {
//by default java will consider all fractional numbers as double
		float a = (float) 60.60;  // typecasting flot into double
		float b = 80.50f;  //  let the compiler know that that this is a float
		double c = 80.50;
		
		
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);

	}

}
