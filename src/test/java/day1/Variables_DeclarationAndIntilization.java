package day1;

public class Variables_DeclarationAndIntilization {

	public static void main(String[] args) {

		//declaring the variable
		boolean status;
		
		//Initializing the variable.
		status = true;
		
		//we can achieve declaraion and intilalizaiotn in one line.
		boolean newstatus = false;
		
		System.out.println("Old Status is "+ status);
		System.out.println("New Status is "+ newstatus);
		
		
		

	}

}
